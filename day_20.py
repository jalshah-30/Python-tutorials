def average(a,b):
    avg= (a+b)/2
    print("Average of two numbers:",avg)
def avg_type(a,b):
    avg=(a+b)/2
    if(avg%2==0):
        print("Average is even")
    else:
        print("Average is odd")

average(5,7)
avg_type(5,7)

average(2,4)
avg_type(2,4)

average(3,4)
